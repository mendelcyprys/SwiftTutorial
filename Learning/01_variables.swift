// a function wrapping the lessons contents
func variables() {
    // 'let' creates constants
    let name = "Eric"
    // 'var' creates mutable variables
    var age = 25
    age = 30
    print("--- Variables ---")
    print("name: \(name)")
    print("age: \(age)")
}
